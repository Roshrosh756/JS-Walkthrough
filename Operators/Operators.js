//Arithmetic Operators
const sum = 5 + 3;      // Addition
const diff = 10 - 2;   // Subtraction
const p = 4 * 2;       // Multiplication
const q = 8 / 2;       // Division
console.log(sum, diff, p, q);       //output : 8 8 8 4

//Assignment Operators
let n = 10;         // = assigns a value to a variable
n += 5;             // += adds and assign the result to variable
n *= 2;             // *= multiply and assigns the result to the variable
console.log(n);     // output : 30

//Comparison Operators
let a = 10;
let b = '10';

console.log(a == b);   // true  only compares value
console.log(a === b);  // false  compares value AND type
console.log(a != b);   // false
console.log(a !== b);  // true
console.log(a > 5);    // true
console.log(a <= 9);   // false

//String Operators
console.log("Hello" + " World"); // Output : Hello World
console.log("Age: " + 25);       //  Output : Age: 25


